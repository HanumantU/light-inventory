DROP TABLE IF EXISTS Product;

CREATE TABLE Product(
    ID SERIAL,
    NAME VARCHAR(200) NOT NULL,
    DESCRIPTION TEXT,
    QUANTITY INT NOT NULL,
    PRICE NUMERIC(8,2) NOT NULL,
    TIME_CREATED TIMESTAMP DEFAULT NOW()
);

