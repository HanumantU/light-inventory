DROP TABLE IF EXISTS Tax;
CREATE TABLE Tax(
    ID SERIAL,
    NAME VARCHAR(200) NOT NULL,
    DESCRIPTION TEXT,
    RATE NUMERIC(8,2) NOT NULL,
    TIME_CREATED TIMESTAMP DEFAULT NOW()
);
