DROP TABLE IF EXISTS Sale;
CREATE TABLE Sale(
    ID SERIAL,
    TOTAL_AMOUNT NUMERIC(8,2) NOT NULL,
    TOTAL_TAX NUMERIC(8,2) NOT NULL,
    SALES_AMOUNT NUMERIC(8,2) NOT NULL,
    TIME_CREATED TIMESTAMP DEFAULT NOW()
);
INSERT INTO Sale( ID, TOTAL_AMOUNT, TOTAL_TAX, SALES_AMOUNT) VALUES (1, 200.00, 20.00, 180.00);

DROP TABLE IF EXISTS ProductSales;
CREATE TABLE ProductSales(
    ID SERIAL,
    SALE_ID INTEGER NOT NULL,
    PRODUCT_ID INTEGER NOT NULL,
    SALE_QUANTITY INTEGER,
    SALE_PRICE NUMERIC(8,2),
    FOREIGN KEY (SALE_ID) REFERENCES Sale(id) ON DELETE CASCADE,
    FOREIGN KEY (PRODUCT_ID) REFERENCES Product(id) ON DELETE CASCADE
);
